#include <unistd.h>

void	ft_print_comb(void)
{
	int		y;
	int		b;
	int		c;

	y = '0';
	while (y <= '7')
	{
		b = y + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				write(1, &y, 1);
				write(1, &b, 1);
				write(1, &c, 1);
				if (y != '7' )
					write(1, &", ", 1);
				c++;
			}
			b++;
		}
		y++;
	}
}
