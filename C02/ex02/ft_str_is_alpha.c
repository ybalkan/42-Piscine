int	ft_str_is_alpha(char *str)
{
	int		y;

	y = 0;
	while (str[y] != '\0')
	{
		if ((str[y] >= 'a' && str[y] <= 'z')
			|| (str[y] >= 'A' && str[y] <= 'Z'))
				y++;
		else
			return (0);
	}
	return (1);
}
