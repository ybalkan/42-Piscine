int	ft_str_is_printable(char *str)
{
	int		y;

	y = 0;
	while (str[y] != '\0')
	{
		if (str[y] >= 32 && str[y] <= 126)
		{
			y++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
